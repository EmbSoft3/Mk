.cpu cortex-m7
.syntax unified

/**
 * @internal
 * @brief
 * @endinternal
 */

.thumb
.thumb_func

.align 4
.global _math_mod2
_math_mod2:

    MOV R3, #0x01
    LSL R3, R3, R1

    LSR R2, R0, R1
    MLS R0, R3, R2, R0
    BX LR



